/*
Laço de repetição: for

For é uma estrutura de repetição que é mais recomendada 
quando você conhece o fim da repetição ou algo é limitado
*/

#include <stdio.h>

int main() {
    // ou declara antes 
    // int i;

    for (int i = 1; i <= 10; i++) {
        printf("Número: %d\n", i);
    }
    
    return 0;
}

